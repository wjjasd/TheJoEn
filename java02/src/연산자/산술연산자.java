package 연산자;

public class 산술연산자 {

	public static void main(String[] args) {
		int x = 200;
		int y = 100;

		System.out.println("더한값" + (x + y));
		System.out.println("뺀값" + (x - y));
		System.out.println("곱한값" + (x * y));
		System.out.println("나눈값" + (x / y));
		System.out.println("나머지" + (x % y));
	}

}

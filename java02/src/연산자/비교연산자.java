package 연산자;

public class 비교연산자 {

	public static void main(String[] args) {
		int x = 200;
		int y = 100;

		System.out.println("같은가? : " + (x == y));
		System.out.println("다른가? : " + (x != y));
		System.out.println("x가 크거나 같은가? : " + (x >= y));
		System.out.println("x가 큰가? : " + (x > y));
	}

}
